package com.beans;

import java.awt.Color;

public class MonGuy {
	private String name;
	private Color skin;
	private float height;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Color getSkin() {
		return skin;
	}
	public void setSkin(Color skin) {
		this.skin = skin;
	}
	public float getHeight() {
		return height;
	}
	public void setHeight(float height) {
		this.height = height;
	}
}
